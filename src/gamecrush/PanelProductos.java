    private void jBtnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnInsertActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO gamecrush.productos(nombre_producto, tipo, precio, proveedor,cantidad) "
                + "VALUES(?,?,?,?,?)";
            Connection conn = Conexion.GetConnection();
            conn.setAutoCommit(false);
            PreparedStatement ps = conn.prepareStatement(sql);
            
            nombre = jTxtFldNombre.getText();
            tipo = (String)jComboBox1.getSelectedItem();
            precio = Double.parseDouble(jTxtFldPrecio.getText());
            proveedor = jTxtFldProveedor.getText();
            cantidad = Integer.parseInt(jTxtFldCantidad.getText());
            
            ps.setString(1, nombre);
            ps.setString(2, tipo);
            ps.setDouble(3, precio);
            ps.setString(4, proveedor);
            ps.setString(5, Integer.toString(cantidad));
            
            ps.execute();
            conn.commit();
            cargarProductos();
            clearTextFields();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getErrorCode()+": "+e.getMessage());
        }
    }//GEN-LAST:event_jBtnInsertActionPerformed

