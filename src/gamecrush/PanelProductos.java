    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "Update gamecrush.productos "
                    + "SET nombre_producto = ?, "
                    + "tipo = ?, "
                    + "precio = ?, "
                    + "proveedor = ?, "
                    + "cantidad = ? "
                    + "WHERE idproductos = ?";
            Connection conn = Conexion.GetConnection();
            conn.setAutoCommit(false);
            PreparedStatement ps = conn.prepareStatement(sql);
            String idProducto = (String)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            this.nombre = jTxtFldNombre.getText();
            this.tipo = (String)jComboBox1.getSelectedItem();
            this.precio = Double.parseDouble(jTxtFldPrecio.getText());
            this.proveedor = jTxtFldProveedor.getText();
            this.cantidad = Integer.parseInt(jTxtFldCantidad.getText());
            
            
            ps.setString(1, nombre);
            ps.setString(2, tipo);
            ps.setDouble(3, precio);
            ps.setString(4, proveedor);
            ps.setInt(5, cantidad);
            ps.setString(6, idProducto);
            ps.executeUpdate();
            
            conn.commit();
            cargarProductos();
            clearTextFields();
            this.jBtnUpdate.setEnabled(false);
            this.jBtnDelete.setEnabled(false);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getErrorCode()+": "+e.getMessage());
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

